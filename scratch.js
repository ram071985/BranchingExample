//// Creating a Working Branch On Git ////

// 1) From the main branch create a new working branch
// git checkout -b working-branch-name

// 2) Add changes and make commit
// git add . 
// git commit -m"changes"

// 3) Push changes to new working branch
// git push origin working-branch-name


//// Merging Changes From Working Branch With Your Main Branch ////

// 1) Checkout of working branch into main branch
// git checkout main

// 2) Merge changes from working branch into main or production branch
// git merge working-branch-name

// Push merged changes from working brnach into remote repository
// git push origin main


